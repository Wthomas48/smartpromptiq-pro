/**
 * Get voice by ID from either ElevenLabs or OpenAI
 */
export function getVoiceById(id: string): VoicePersona | undefined {
  return ELEVENLABS_VOICES.find(v => v.id === id) || OPENAI_VOICES.find(v => v.id === id);
}

/**
 * Get voices for a specific category
 */
export function getVoicesForCategory(category: keyof typeof VOICE_CATEGORIES): VoicePersona[] {
  const voiceIds = VOICE_CATEGORIES[category] || VOICE_CATEGORIES.general;
  return voiceIds.map(id => getVoiceById(id)).filter(Boolean) as VoicePersona[];
}

